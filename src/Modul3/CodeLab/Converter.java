package Modul3.CodeLab;
public class Converter {
    protected double setValueToM(double params){
        return params * 0.9144;
    }
    protected double setValueToCM(double params){
        return params * 91.44;
    }
    protected double setValueToFeet(double params){
        return params * 3;
    }
    protected double setValueToMiles(double params){
        return params * 0.000568181818;
    }
}
